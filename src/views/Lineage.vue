    <h3>
      New Beginnings
    </h3>

    <p>
      Once we had discovered Regardie’s book The Golden Dawn, there was no
      denying that we had indeed found our true spiritual path. Here was a
      system of magic that was effective and efficient. Everything about the
      Golden Dawn tradition seemed harmonious and elegant—every piece of the
      system fit perfectly with every other piece, like a finely-crafted
      symphony. The richness of the Golden Dawn’s teachings was evident on every
      page. Prayers, invocations, and the sheer poetry of such phrases as “By
      Names and Images are all Powers Awakened and Reawakened” resonated deeply
      and drew us in like moths to a flame. We were hooked.
    </p>

    <p>
      However, it was the mid-1970s, and the Golden Dawn, as a magical Order,
      seemed to have been dead for several decades. It was at this point that
      our temple, a new temple of the Golden Dawn, was formed in Columbus,
      Georgia, using Regardie’s book The Golden Dawn as a guide. In 1977 we
      founded a Golden Dawn temple and rented a house that would be used only
      for magical work. Located on an acre of land, the temple house was set
      back away from the road in a grove of old Pecan trees. One day while we
      were exploring all the rooms in the house, we came upon a hidden door
      behind one of the walls in the attic. This led into a small room over
      the carport, where we found an Altar and some magical implements dating
      back to the early 1930’s.  Someone had been using this hidden room for
      Masonic rituals.  We took this to be a good omen!
    </p>

    <p>
      Work on the temple rooms began immediately, starting with the Neophyte
      Hall and the Vault of the Adepti. The dining room was transformed into a
      Neophyte Hall and the parlor became the filled with the framework for the
      Vault. Our temple was christened Isis-Urania, after the original London
      temple of Mathers, Westcott, and Woodman. We formed a study group within
      the temple to examine the rituals and knowledge lectures in Regardie’s
      book. When the Neophyte Hall was finished, we initiated ourselves into the
      0=0 grade. We then initiated about fourteen others.
    </p>

    <p>
      About a year after we had established our Golden Dawn temple, we came into
      contact with the Ordo Templi Orientis. Convinced that the Hermetic Order
      of the Golden Dawn was dead outside of our own local temple, we joined the
      OTO to supplement our esoteric studies.  Shortly thereafter, half of the
      temple house was dedicated to the OTO, and half was dedicated to the
      Golden Dawn. (It was truly a house divided!) Eventually Grady McMurty, the
      Caliph of the OTO, gave us Israel Regardie’s mailing address so that we
      could contact him. We were stunned to learn that Israel Regardie was alive
      and well and living in Sedona, Arizona!
    </p>

    <p>
      Our correspondence with Regardie began in 1981. In a letter dated
      September 26, 1981, Regardie expressed his excitement about the prospect
      of seeing our Vault: “The truth is I haven’t seen a Vault since about
      1935, and if your Vault is a functioning one, with a pastos, etc. one of
      these days, if and when I feel more like a human being, and have a few
      excess bucks, I might pay Columbus, Georgia a visit just to see your
      temple and the Vault.” Regardie was very interested in the Vault’s
      progress. He sent us an Order manuscript describing the symbolism of the
      Vault to help us in completing the structure. By May of 1982, the Vault
      was finished.
    </p>

    <p>
      We first met Regardie, who insisted that we call him “Francis,” in June of
      1982 when he came to visit us for a week of workshops and initiations. Our
      temple members were treated to several lectures on the Qabalah, the
      history of the Order, the Pentagram Ritual, and other topics.
    </p>

    <p>
      When we had originally decorated the temple house in 1977, we unknowingly
      placed a reproduction of an old Rembrandt painting on the closet door
      which led into the Second Order chamber and the Vault of the Adepti. While
      Regardie was visiting our temple we discovered that this painting,
      entitled “A man in Armour,” was said to represent none other than
      Christian Rosenkreutz. Another good omen!
    </p>

    <p>
      On June 24, 1982, the day of Corus Christi, all members of Isis-Urania
      gathered at the Temple. Acting as Chief Adept, Regardie consecrated our
      Vault. In the days that followed, Regardie performed two Adeptus Minor
      initiations, establishing an initiatory lineage and a fully functioning
      Second Order. After that he assumed the office of Hierophant, consecrated
      our Neophyte Hall, and performed a Neophyte initiation. It was the only
      time that Regardie ever took on these offices and performed these
      ceremonies; they were the only Golden Dawn initiations he ever performed.
      Immediately after this eventful week, we travelled with Regardie to New
      Orleans and spent several days relaxing and celebrating the resurrection
      of the Golden Dawn in the U.S.A.
    </p>

    <p>
      Our friendship with Regardie resulted in many long phone conversations as
      well as several trips to Arizona—including a two-week visit in 1982, three
      one-week visits in 1983, two one-week visits in 1984, and a final two-week
      visit in 1985 only ten days before Regardie’s death.
    </p>

    <p>
      A typical day with Israel Regardie was spent in laughter and long
      discussions on magic which would last for hours. During teaching sessions,
      Regardie covered different aspects of Golden Dawn magic, including the
      correct way to project energy, how to charge talismans using the
      Projection Sign and the Sign of Silence, as well as talismans using the
      Middle Pillar ritual, different methods of healing, and the Opening by
      Watchtower, a ritual that Regardie had written.
    </p>

    <p>
      Regardie’s sudden death on March 10, 1985, only days after our final visit
      to his Arizona home, was a huge shock. Days later, we performed the
      Requiem Ceremony from The Golden Dawn in his honor, in conjunction with
      the reading of one of his favorite pieces, “The Prayer of the Sylphs” from
      the Theoricus Ritual. But the work that Regardie had started in
      resurrecting the Order was destined to continue.
    </p>

    <p>
      By that time, three of us who had been a part of that momentous weekend in
      Georgia in 1982, had already established functioning Golden Dawn temples
      and were working together as an Order (the Isis-Urania Temple and another
      Southern temple, both of which were founded in the 1970s, and the Osiris
      Khenti Amenti Temple in Los Angeles which was founded in 1982). Shortly
      after Regardie’s death, we decided to form a traditional triad of Order
      Chiefs, coequal in authority, who would govern all of the temples working
      with us. Daughter temples were eventually founded in other states and in
      other countries.
    </p>

    <p>
      As our personal involvement with the Golden Dawn began to increase, our
      interest in the OTO began to decline. Two things were fundamental to our
      decision to focus on the Golden Dawn as our primary spiritual path. The
      first was the elegance of the Golden Dawn system itself, and the fact that
      it had always been our main path from the beginning. The second was Israel
      Regardie—his friendship, mentoring, and guidance.
    </p>

    <h3>
      The Light Extended
    </h3>

    <p>
      As friends and students of Israel Regardie, we are grateful to have
      received certain documents which bring Regardie’s Golden Dawn legacy full
      circle. In 1997 we were given a charter for all the esoteric lineages held
      by the Societas Rosicruciana in America, the organization which initiated
      Regardie’s search for the Golden Dawn back in 1926. We were also given the
      complete archives of Regardie’s personal documents and letters which were
      formerly owned by the Israel Regardie Foundation. We consider the single
      greatest value in all of these items to be the connection they provide us
      to the one man who was so instrumental in our own personal quest for the
      Light—our beloved Frater A.M.A.G.
    </p>

    <p>
      Today, the Hermetic Order of the Golden Dawn is alive and thriving. The
      Order serves as a custodian of the Hermetic Tradition, keeping the
      knowledge of this tradition intact, while at the same time instructing
      those individuals called to the Initiatory Path of the Mysteries. By
      definition, the various strands of the Hermetic Tradition include the
      ancient Egyptian, Græco-Egyptian, and Græco-Roman mystery religions;
      Neoplatonism, Iamblichan theurgy, Gnosticism, Qabalah, alchemy,
      Rosicrucianism, and Esoteric Christianity. This tradition holds a belief
      in both the macrocosm and the microcosm and seeks to comprehend both. It
      is ultimately monotheistic, although it conceives of the ultimate
      Divinity, which is both immanent and transcendent, as emanating through
      various forms and expressions. The curriculum of the modern-day Order has
      grown well beyond the material published by Regardie and others, while
      remaining true to the Osirian/Rosicrucian tradition, spirit, structure,
      and fundamental vision of William Wynn Westcott, Samuel Liddell MacGregor
      Mathers and William Robert Woodman.
    </p>

    <p>
      Our Order does not solicit for membership, and initiation is by invitation
      only. Nevertheless, H.O.G.D. promotes tolerance for all spiritual paths
      and holds to the fundamental Rosicrucian principle that spiritual wisdom
      should be given gratis. And we hold to the view that Spiritual Wisdom may
      be gained from the most unexpected of sources. As the old axiom reminds
      us, “when the student is ready, the teacher will appear.”
    </p>
